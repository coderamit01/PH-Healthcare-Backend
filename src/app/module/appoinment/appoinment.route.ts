import { Router } from "express";
import { authCheck } from "../../middleware/authCheck";
import { Role } from "../../../generated/prisma/enums";
import { AppointmentController } from "./appoinment.controller";

const router = Router();

router.post(
  "/book-appointment",
  authCheck(Role.PATIENT),
  AppointmentController.bookAppointment,
);
router.get(
  "/my-appointments",
  authCheck(Role.PATIENT, Role.DOCTOR),
  AppointmentController.getMyAppointments,
);
router.patch(
  "/change-appointment-status/:id",
  authCheck(Role.PATIENT, Role.DOCTOR, Role.ADMIN, Role.SUPER_ADMIN),
  AppointmentController.changeAppointmentStatus,
);
router.get(
  "/my-single-appointment/:id",
  authCheck(Role.PATIENT, Role.DOCTOR),
  AppointmentController.getMySingleAppointment,
);
router.get(
  "/all-appointments",
  authCheck(Role.ADMIN, Role.SUPER_ADMIN),
  AppointmentController.getAllAppointments,
);
router.post(
  "/book-appointment-with-pay-later",
  authCheck(Role.PATIENT),
  AppointmentController.bookAppointmentWithPayLater,
);
router.post(
  "/initiate-payment/:id",
  authCheck(Role.PATIENT),
  AppointmentController.initiatePayment,
);

export const AppoinmentRoutes = router;
